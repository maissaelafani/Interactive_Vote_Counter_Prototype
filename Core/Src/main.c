bool read_button() { // Lire l'état de la broche du bouton poussoir
    GPIO_PinState button_state = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_4);

    // Convertir l'état de la broche en un booléen (true pour enfoncé, false pour relâché)
    if (button_state == GPIO_PIN_SET) {
        return true; // Bouton enfoncé
    } else {
        return false; // Bouton relâché
    }
}


